x = (23, 43, 67, 77, 23, 45, 87)
n = len(x)        # Passo 1: Obter o número de elementos
somatorio = 0     # Inicializar a soma

# Passo 2: Calcular a soma dos elementos
for i in x:
    somatorio += i

# Passo 3: Calcular a média
media = somatorio / n

# Passo 4: Imprimir a média
print(f'Média: {media:.2f}')


